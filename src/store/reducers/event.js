const reducer = (state = initialState, action) => {
    switch (action.type) {
        case SET_EVENT_TRACKING:
            const index = action.data.index;
            return {
                ...state,
                event: updateEvent(state.event, index)
            };
        default:
            return state;
    };
};

const updateEvent = (itemArray, index) => {
    return itemArray.map((item, arrayIndex) => {
        if (arrayIndex == index) {
            item.tracking = !item.tracking
        }

        return item
    });
}

export default reducer